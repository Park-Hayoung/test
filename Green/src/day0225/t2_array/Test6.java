package day0225.t2_array;

public class Test6 {
	public static void main(String[] args) {
		// 정수형 배열의 초기값은 0으로 들어있다.
		int[] atom1 = new int[5];
		System.out.println("atom1[0] : " + atom1[0]);
		System.out.println();
		
		// 실수형 배열의 초기값은 0.0으로 들어있다.
		double[] atom2 = new double[5];
		System.out.println("atom2[0] : " + atom2[0]);
		System.out.println();
		
		// 실수형 배열의 초기값은 0.0으로 들어있다.
		float[] atom5 = new float[5];
		System.out.println("atom5[0] : " + atom5[0]);
		System.out.println();

		// boolean 배열의 초기값은 false 로 들어가있다.
		boolean[] atom3 = new boolean[5];
		System.out.println("atom3[0] : " + atom3[0]);
		System.out.println();
		
		//String 배열의 초기값은 null이 들어있다.
		String[] atom4 = new String[5];
		System.out.println("atom4[0] : " + atom4[0]);
		System.out.println();
		
		
		
	}
}
